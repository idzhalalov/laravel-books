     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $books = Book::leftJoin(
                'authors',
                'authors.id',
                '=',
                'books.author_id')
            ->orderBy('id', 'desc')
            ->limit(6)
            ->get([
                'books.id',
                'books.title',
                'books.author_id',
                'authors.full_name'
            ]);

        $authors = Author::orderBy('id', 'desc')->limit(3)->get();

        return view('home', [
            'books' => $books,
            'authors' => $authors
        ]);
    }

